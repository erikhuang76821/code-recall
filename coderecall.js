// ---------------------------------------------------------------------------
// Git pre-commit integration (optional; code maintains derived state)
//
// Rationale: derived/index state should be regenerated by code, not by the
// agent's goodwill. This hook refreshes the AGENTS.md digest and lints the
// ledger at commit time. Implemented as coderecall.js subcommands (node writes the
// hook file) so it behaves identically on Windows (Git for Windows runs hooks
// through its bundled sh) and POSIX — no per-OS shell duplication.
// ---------------------------------------------------------------------------
const GITHOOK_BEGIN = '# >>> coderecall >>> (managed block — installed by: node coderecall.js install-githook)';
const GITHOOK_END = '# <<< coderecall <<<';

/** Run `git <args>` in CWD, returning trimmed stdout, or null on any failure. */
function gitOut(args) {
  try {
    return require('child_process').execFileSync('git', args, { cwd: CWD, encoding: 'utf8', stdio: ['ignore', 'pipe', 'ignore'] }).trim();
  } catch (e) { return null; }
}

/** Resolve the repo's hooks directory (honors core.hooksPath), or null. */
function gitHooksDir() {
  const top = gitOut(['rev-parse', '--show-toplevel']);
  if (top === null) return null;
  let hooks = gitOut(['rev-parse', '--git-path', 'hooks']);
  if (hooks === null) return null;
  // --git-path may return a path relative to CWD; resolve against CWD.
  return path.resolve(CWD, hooks);
}

/** Forward-slash an absolute path (sh-friendly, valid for node on Windows too). */
function fwd(p) { return String(p).replace(/\\/g, '/'); }

/** The managed sh block embedded in .git/hooks/pre-commit. strict → blocking. */
function gitHookBlock(strict) {
  const node = fwd(process.execPath);
  const memo = fwd(path.join(__dirname, 'coderecall.js'));
  // Shell-safety guard mirrors the installers: a path with " ` $ or a newline
  // could break out of the double-quoting in the generated sh.
  if (/["`$\n\r]/.test(node) || /["`$\n\r]/.test(memo)) {
    fail('cannot install git hook: a path contains shell-unsafe characters (" ` $ or newline)\n  node: ' + node + '\n  memo: ' + memo);
  }
  return [
    GITHOOK_BEGIN,
    'if [ -d .ai/memory ]; then',
    '  _memo_node="' + node + '"',
    '  [ -x "$_memo_node" ] || _memo_node="$(command -v node 2>/dev/null)"',
    '  if [ -n "$_memo_node" ]; then',
    '    "$_memo_node" "' + memo + '" precommit' + (strict ? ' --strict' : '') + ' || exit 1',
    '    # Stage the refreshed digest for files git already tracks (so the',
    '    # committed AGENTS.md/CLAUDE.md match TASK.md). Untracked files are',
    '    # left alone. if/then/fi (not &&) keeps exit status 0 on a non-match,',
    '    # otherwise a final non-zero check would abort the whole commit.',
    '    for _memo_f in AGENTS.md CLAUDE.md; do',
    '      if git ls-files --cached --error-unmatch "$_memo_f" >/dev/null 2>&1; then git add "$_memo_f"; fi',
    '    done',
    '  fi',
    'fi',
    GITHOOK_END,
  ].join('\n');
}

