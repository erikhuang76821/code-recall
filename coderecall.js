// `coderecall check` (write-back primitive ③): a read-only report of the gap
// between the ledger's working state and what the repo has actually done since
// UPDATED. Advisory by default (exit 0); `--strict` exits 1 when stale, for a
// CI / pre-push gate. Reuses ledgerStale() for the verdict so this CLI and the
// SessionStart digest can never disagree.
function cmdCheck(strict) {
  requireLedger();
  const taskText = readFileSafe(TASK_FILE);
  if (taskText === null) fail('TASK.md missing. Run: node coderecall.js init');
  const t = parseTask(taskText);
  const verdict = ledgerStale(t);
  const age = taskAgeHours(t);

  console.log('coderecall check — write-back status');
  console.log('  ledger UPDATED: ' + (t.updated || '(none)') + (age !== null ? '  (' + age.toFixed(1) + 'h ago)' : ''));
  if (gitOut(['rev-parse', '--is-inside-work-tree']) === 'true') {
    const srcPathspec = ['.', ':!.ai/memory'];
    if (t.updated && !isNaN(Date.parse(t.updated))) {
      const log = gitOut(['log', '--since=' + t.updated, '--format=%h', '--'].concat(srcPathspec));
      if (log !== null) console.log('  source commits since UPDATED: ' + (log === '' ? 0 : log.split('\n').length));
    }
    const dirty = gitOut(['status', '--porcelain', '--'].concat(srcPathspec));
    if (dirty !== null) console.log('  uncommitted source files: ' + (dirty === '' ? 0 : dirty.split('\n').filter(Boolean).length));
  } else {
    console.log('  (not a git work tree — staleness falls back to the ' + STALE_HOURS + 'h clock)');
  }
  console.log('  verdict: ' + (verdict.stale ? 'STALE — ' + verdict.reason : 'fresh — ' + verdict.reason));
  console.log('');
  console.log('  NOW:  ' + (t.now || '(not set)'));
  console.log('  NEXT: ' + (t.next || '(not set)'));
  if (verdict.stale) {
    console.log('');
    console.log('  → The repo moved past the ledger. Update .ai/memory/TASK.md (NOW:/NEXT: + checklist),');
    console.log('    record any decision in DECISIONS.md, then re-run check.');
  }
  if (strict && verdict.stale) process.exit(1);
}

// ---------------------------------------------------------------------------
// Working-state score — is the ledger actually telling an agent what to do?
//
// Deliberately a transparent heuristic, not a fake-precision ML number: every
// dimension states WHY and weights toward actionability (a vague NEXT scores
// low even if all fields are "filled in"). The point is to catch false
// completeness — a ledger that looks done but can't drive the next action.
// ---------------------------------------------------------------------------
function scoreWorkingState() {
  const t = parseTask(readFileSafe(TASK_FILE) || '') || { goal: '', now: '', next: '', lines: [] };
  const dims = [];
  const isPlaceholder = (s) => !s || /^<.*>$/.test(String(s).trim()) || String(s).trim() === '(not set)';
  const VAGUE = /\b(tbd|todo|wip|continue|finish (it|up)|keep going|more work|stuff|various|misc|etc)\b/i;

  // GOAL — is the objective stated?
  const g = (t.goal || '').trim();
  if (isPlaceholder(g)) dims.push({ key: 'goal', w: 25, score: 0, why: 'GOAL unset/placeholder — state the one-line objective' });
  else if (g.length < 12) dims.push({ key: 'goal', w: 25, score: 55, why: 'GOAL very short — make it specific' });
  else dims.push({ key: 'goal', w: 25, score: 100, why: 'GOAL is specific' });

  // NOW — is the current activity stated?
  const n = (t.now || '').trim();
  if (isPlaceholder(n)) dims.push({ key: 'now', w: 15, score: 0, why: 'NOW unset — say exactly what is in progress' });
  else dims.push({ key: 'now', w: 15, score: n.length < 8 ? 60 : 100, why: n.length < 8 ? 'NOW is terse' : 'NOW set' });

  // NEXT — the load-bearing field. Vague = useless even when "filled in".
  const x = (t.next || '').trim();
  if (isPlaceholder(x)) dims.push({ key: 'next', w: 25, score: 0, why: 'NEXT unset — give the immediate next action' });
  else if (VAGUE.test(x) || x.length < 12) dims.push({ key: 'next', w: 25, score: 45, why: 'NEXT is vague — make it one concrete next action' });
  else dims.push({ key: 'next', w: 25, score: 100, why: 'NEXT is concrete' });

  // BLOCKERS — conditional: only matters when something is blocked; each [!]
  // must carry a reason/owner (a bare blocked item is dead weight).
  const blocked = (t.lines || []).filter((l) => /^\s*- \[!\]/.test(l));
  if (blocked.length === 0) dims.push({ key: 'blockers', w: 15, score: 100, why: 'no open blockers' });
  else {
    const withReason = blocked.filter((l) => {
      const after = l.replace(/^\s*- \[!\]\s*/, '');
      return after.length > 15 || /[—:]|--| - /.test(after);
    });
    const ratio = withReason.length / blocked.length;
    dims.push({ key: 'blockers', w: 15, score: Math.round(ratio * 100), why: ratio < 1 ? (blocked.length - withReason.length) + ' blocked item(s) lack a reason — add "— why / owner"' : 'blockers all have reasons' });
  }

  // FRESHNESS — stale state silently misleads.
  const age = taskAgeHours(t);
  if (age === null) dims.push({ key: 'freshness', w: 15, score: 0, why: 'UPDATED missing/unparseable' });
  else if (age <= STALE_HOURS) dims.push({ key: 'freshness', w: 15, score: 100, why: 'fresh (' + age.toFixed(1) + 'h)' });
  else if (age <= 6) dims.push({ key: 'freshness', w: 15, score: 60, why: age.toFixed(1) + 'h old — consider re-anchoring' });
  else if (age <= 24) dims.push({ key: 'freshness', w: 15, score: 30, why: age.toFixed(1) + 'h stale — re-anchor NOW/NEXT' });
  else dims.push({ key: 'freshness', w: 15, score: 10, why: age.toFixed(1) + 'h stale' });

  // GROUNDING (minor) — is the work backed by recorded reasoning?
  const dN = parseEntries(readFileSafe(DECISIONS_FILE) || '').entries.filter((e) => entryStatus(e) !== 'superseded').length;
  const lN = parseEntries(readFileSafe(LESSONS_FILE) || '').entries.filter((e) => entryStatus(e) !== 'superseded').length;
  dims.push({ key: 'grounding', w: 5, score: (dN + lN) > 0 ? 100 : 60, why: (dN + lN) > 0 ? dN + ' decisions, ' + lN + ' lessons on file' : 'no decisions/lessons recorded yet' });

  const wsum = dims.reduce((a, d) => a + d.w, 0);
  let overall = Math.round(dims.reduce((a, d) => a + d.score * d.w, 0) / wsum);
  // Empty-ledger guard: when all three load-bearing fields are placeholders, the
  // "free" support dims (freshness on a just-created file, no-blockers because
  // there's nothing, grounding) would otherwise pad the score to ~33 — false
  // comfort for a ledger that is 0% usable. Floor it so the number is honest.
  const core = dims.filter((d) => d.key === 'goal' || d.key === 'now' || d.key === 'next');
  const emptyCore = core.every((d) => d.score === 0);
  if (emptyCore) overall = Math.min(overall, 8);
  return { overall, dims, emptyCore };
}

function cmdScore(asJson) {
  requireLedger();
  const r = scoreWorkingState();
  if (asJson) { process.stdout.write(JSON.stringify(r) + '\n'); return; }
  console.log('coderecall score: ' + r.overall + ' / 100   (working-state health)' +
    (r.emptyCore ? '   — empty ledger: fill GOAL / NOW / NEXT to begin' : ''));
  console.log('');
  for (const d of r.dims) {
    console.log('  ' + d.key.padEnd(10) + ' ' + String(d.score).padStart(3) + '  ' + d.why);
  }
  console.log('');
  const weak = r.dims.filter((d) => d.score < 60).sort((a, b) => a.score - b.score).map((d) => d.key);
  console.log(weak.length ? 'Fix first: ' + weak.join(', ') : 'Working state is healthy — an agent can pick it up cold.');
}

/**
 * Ledger format lint: DECISIONS/LESSONS entries must carry a valid
 * `- date: YYYY-MM-DD` and `- confidence: high|med|low`; TASK checklist lines
 * must use one of [ ] [>] [x] [!]. Returns an array of human-readable issue
 * strings (empty = clean). Shared by `doctor` and `precommit`.
 */
function ledgerLintIssues() {
  const issues = [];
  for (const [label, p] of [['DECISIONS.md', DECISIONS_FILE], ['LESSONS.md', LESSONS_FILE]]) {
    const text = readFileSafe(p);
    if (text === null) continue;
    for (const e of parseEntries(text).entries) {
      const body = e.lines.join('\n');
      const stMatch = /^- status:\s*(\S+)\s*$/m.exec(body);
      if (!/^- date:\s*\d{4}-\d{2}-\d{2}\s*$/m.test(body)) issues.push(label + ' entry "' + e.title + '" — bad/missing date');
      else if (!/^- confidence:\s*(?:high|med|low)\s*$/m.test(body)) issues.push(label + ' entry "' + e.title + '" — bad/missing confidence');
      else if (stMatch && !/^(?:proposed|accepted|superseded|deprecated)$/.test(stMatch[1])) issues.push(label + ' entry "' + e.title + '" — invalid status "' + stMatch[1] + '" (proposed|accepted|superseded|deprecated)');
    }
  }
  for (const l of splitLines(readFileSafe(TASK_FILE) || '')) {
    const m = /^\s*- \[(.)\]/.exec(l);
    if (m && ' >x!'.indexOf(m[1]) === -1) issues.push('TASK.md checklist invalid state "[' + m[1] + ']"');
  }
  return issues;
}

function cmdDoctor() {
  requireLedger();
  let warnings = 0;
  const warn = (msg) => { warnings++; console.log('  WARN ' + msg); };
  const ok = (msg) => console.log('  ok   ' + msg);
  console.log('coderecall doctor');
  console.log('');

  // 1. Ledger files + budgets
  console.log('[ledger]');
  for (const [label, p] of [['TASK.md', TASK_FILE], ['DECISIONS.md', DECISIONS_FILE], ['LESSONS.md', LESSONS_FILE]]) {
    const text = readFileSafe(p);
    if (text === null) { warn(label + ' missing — run: node coderecall.js init'); continue; }
    const bytes = Buffer.byteLength(text, 'utf8');
    if (bytes > LEDGER_WARN_BYTES) warn(label + ' is ' + bytes + ' bytes (> ' + LEDGER_WARN_BYTES + '); run: node coderecall.js consolidate');
    else ok(label + ' ' + bytes + ' bytes (~' + tokenEstimate(text) + ' tokens)');
  }
  const t = parseTask(readFileSafe(TASK_FILE) || '');
  const age = taskAgeHours(t);
  if (age === null) warn('TASK.md UPDATED line missing or unparseable');
  else if (age > STALE_HOURS && t.goal && !t.goal.startsWith('<')) warn('TASK.md is ' + age.toFixed(1) + 'h old — ledger may be stale');
  else ok('TASK.md freshness ' + age.toFixed(1) + 'h');
  // GOAL set but NOW unparseable → the digest has no current-state anchor (the
  // exact failure that silently stranded LevelTest: "NOW【…】" with no colon).
  const goalIsSet = t.goal && !/^<.*>$/.test(t.goal) && t.goal !== '(not set)';
  if (goalIsSet && !t.now) warn('TASK.md GOAL is set but NOW: could not be parsed — check for a missing colon (use "NOW: <one line>"); the digest re-anchor will be empty');
  // Multiple NOW/NEXT lines ⇒ the file is being APPENDED to, not rewritten in place.
  // NOW must be the single current state; a log of finished steps belongs in
  // sessions.md / DECISIONS.md. Left unchecked this grows TASK.md without bound
  // (LevelTest reached 60KB / 15× the byte budget this way).
  if (t.nowLines > 1 || t.nextLines > 1) warn('TASK.md has ' + t.nowLines + ' NOW + ' + t.nextLines + ' NEXT lines — rewrite NOW:/NEXT: in place, do not append; move finished-step logs to sessions.md/DECISIONS.md');
  // Stale-but-parseable NOW: looks like a completed-work log, not a current task.
  if (t.now && NOW_LOOKS_DONE_RE.test(t.now)) warn('TASK.md NOW: contains completion markers (✓/已完成/已合併) — if that work is done, replace NOW: with the current task so the digest re-anchors to live work');

  // 2. Marker section drift
  console.log('[sync]');
  const drift = sectionDrift(AGENTS_FILE);
  if (drift === 'ok') ok('AGENTS.md marker section checksum matches');
  else warn('AGENTS.md marker section: ' + drift + ' — run: node coderecall.js sync');
  // Codex CLI reads only ~32KiB of AGENTS.md; a bloated file silently truncates.
  const agentsText = readFileSafe(AGENTS_FILE);
  if (agentsText !== null) {
    const ab = Buffer.byteLength(agentsText, 'utf8');
    if (ab > CODEX_AGENTS_WARN_BYTES) warn('AGENTS.md is ' + ab + ' bytes (> ' + CODEX_AGENTS_WARN_BYTES + '); Codex CLI may truncate it — trim or consolidate');
    else ok('AGENTS.md ' + ab + ' bytes (within Codex ~32KiB read window)');
  }

  // 2b. Format lint — entries must carry a valid date + confidence; TASK
  // checklist states must be one of [ ] [>] [x] [!]. Malformed entries break
  // dedupe/age-flagging and the digest counts.
  console.log('[lint]');
  for (const [label, p] of [['DECISIONS.md', DECISIONS_FILE], ['LESSONS.md', LESSONS_FILE]]) {
    const text = readFileSafe(p);
    if (text !== null) ok(label + ' — ' + parseEntries(text).entries.length + ' entr(ies)');
  }
  const lintIssues = ledgerLintIssues();
  if (lintIssues.length === 0) ok('all entries + checklist states well-formed');
  else for (const m of lintIssues) warn(m);

  // 3. Hooks in ~/.claude/settings.json
  // (path overridable via CODE_RECALL_SETTINGS for testing; default unchanged)
  console.log('[hooks]');
  const settingsPath = process.env.CODE_RECALL_SETTINGS ||
    path.join(os.homedir(), '.claude', 'settings.json');
  const settingsText = readFileSafe(settingsPath);
  let sessionStartCmd = null;
  if (settingsText === null) {
    warn('~/.claude/settings.json not found — run install.ps1 to register hooks');
  } else {
    let settings = null;
    try { settings = JSON.parse(settingsText); } catch (e) {
      warn('~/.claude/settings.json is not valid JSON: ' + e.message);
    }
    if (settings) {
      const hooks = settings.hooks || {};
      // Two-way detection mirroring install.ps1/install.sh: an entry is ours if
      // its command mentions "coderecall" OR this repo's own hooks directory
      // (covers repos checked out under a folder not named coderecall).
      // JSON-escape the path needle because the haystack is JSON.stringify'd
      // (backslashes in Windows paths are doubled there). Compare lowercased —
      // win32 paths are case-insensitive; on POSIX this is merely lenient.
      const hooksDirNeedle = JSON.stringify(path.join(__dirname, 'hooks')).slice(1, -1).toLowerCase();
      for (const event of ['SessionStart', 'PreCompact', 'Stop']) {
        const list = Array.isArray(hooks[event]) ? hooks[event] : [];
        let registered = false;
        for (const entry of list) {
          let es = '';
          try { es = JSON.stringify(entry || {}).toLowerCase(); } catch (e) { es = ''; }
          if (es.includes('coderecall') || es.includes(hooksDirNeedle)) {
            registered = true;
            if (event === 'SessionStart' && sessionStartCmd === null &&
                entry && Array.isArray(entry.hooks)) {
              for (const h of entry.hooks) {
                if (h && typeof h.command === 'string' && h.command) {
                  sessionStartCmd = h.command;
                  break;
                }
              }
            }
          }
        }
        if (registered) ok(event + ' hook registered');
        else warn(event + ' hook not registered — run install.ps1');
      }
    }
  }

  // 4. Hook execution (end-to-end): actually run the registered SessionStart
  // command through a shell (cmd on Windows, sh on POSIX — execSync default)
  // with synthetic hook stdin pointing at a temp cwd WITHOUT .ai/memory, so a
  // healthy hook exits 0 instantly. Catches dead node paths, moved repos, etc.
  console.log('[hook execution]');
  if (sessionStartCmd === null) {
    console.log('  info hook execution check skipped (no coderecall SessionStart hook registered)');
  } else {
    const cp = require('child_process');
    let tmpDir = null;
    try {
      tmpDir = fs.mkdtempSync(path.join(os.tmpdir(), 'coderecall-doctor-'));
      const stdinJson = JSON.stringify({
        hook_event_name: 'SessionStart',
        source: 'startup',
        cwd: tmpDir,
      });
      cp.execSync(sessionStartCmd, {
        input: stdinJson,
        timeout: 5000,
        cwd: tmpDir,
        stdio: ['pipe', 'pipe', 'pipe'],
      });
      ok('SessionStart hook command executed successfully (exit 0)');
    } catch (e) {
      const detail = e && e.stderr ? String(e.stderr).trim() : '';
      warn('SessionStart hook command FAILED: ' + (e && e.message ? e.message : String(e)) +
        (detail ? ' — ' + detail.slice(0, 200) : ''));
    } finally {
      if (tmpDir) { try { fs.rmdirSync(tmpDir); } catch (e2) { /* best effort */ } }
    }
  }

  console.log('');
  console.log(warnings === 0 ? 'All checks passed.' : warnings + ' warning(s). See above.');
}

