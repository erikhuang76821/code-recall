// ---------------------------------------------------------------------------
// Concurrency: atomic writes + directory lock
// ---------------------------------------------------------------------------
const LOCK_STALE_MS = 10000;   // a lock older than 10s (mtime) is stale — break it
const LOCK_RETRIES = 10;       // default acquire attempts
const LOCK_RETRY_DELAY_MS = 50;

/**
 * Atomic file write: write to <file>.tmp.<pid> then rename over the target.
 * rename() is atomic on both NTFS and POSIX, so readers never observe a
 * half-written file and concurrent writers can't interleave bytes.
 */
function writeFileAtomic(file, content) {
  const tmp = file + '.tmp.' + process.pid;
  fs.writeFileSync(tmp, content, 'utf8');
  try {
    fs.renameSync(tmp, file);
  } catch (e) {
    try { fs.unlinkSync(tmp); } catch (e2) { /* best effort */ }
    throw e;
  }
}

/** Synchronous sleep without busy-spinning (Atomics.wait is allowed on Node's main thread). */
function sleepMs(ms) {
  // Some embedders / hardened runtimes ship without SharedArrayBuffer (or
  // with Atomics.wait disabled). Guard explicitly and fall back to a
  // Date.now() busy-wait — acceptable here because lock waits are short
  // (50ms) and rare. Behavior is identical when SAB is available.
  if (typeof SharedArrayBuffer !== 'undefined' &&
      typeof Atomics !== 'undefined' && typeof Atomics.wait === 'function') {
    try {
      Atomics.wait(new Int32Array(new SharedArrayBuffer(4)), 0, 0, ms);
      return;
    } catch (e) { /* fall through to busy wait */ }
  }
  const end = Date.now() + ms;
  while (Date.now() < end) { /* busy wait fallback (short 50ms waits only) */ }
}

// Re-entrant within one process: nested acquireLock() calls just bump a depth
// counter, so e.g. consolidate -> touchTaskUpdated never self-deadlocks.
let lockDepth = 0;

function lockDirPath() {
  return path.join(MEM_DIR, '.lock');
}

/**
 * Acquire the ledger lock via fs.mkdirSync (atomic on NTFS/POSIX).
 * opts: { retries (default 10), delayMs (default 50) }.
 * Stale locks (mtime older than LOCK_STALE_MS) are broken.
 * Returns true on success, false when the lock stayed contended — callers
 * (hooks especially) must then SKIP their write rather than block.
 */
function acquireLock(opts) {
  opts = opts || {};
  const retries = (opts.retries === undefined) ? LOCK_RETRIES : opts.retries;
  const delayMs = (opts.delayMs === undefined) ? LOCK_RETRY_DELAY_MS : opts.delayMs;
  if (lockDepth > 0) { lockDepth++; return true; }
  const lockDir = lockDirPath();
  for (let attempt = 0; attempt <= retries; attempt++) {
    try {
      fs.mkdirSync(lockDir);
      lockDepth = 1;
      return true;
    } catch (e) {
      // Stale-lock breaking: a crashed holder leaves the dir behind forever.
      try {
        const st = fs.statSync(lockDir);
        if (Date.now() - st.mtimeMs > LOCK_STALE_MS) {
          try { fs.rmdirSync(lockDir); } catch (e2) { /* raced removal */ }
          continue; // retry immediately
        }
      } catch (e2) {
        continue; // lock vanished between mkdir and stat — retry immediately
      }
      if (attempt < retries) sleepMs(delayMs);
    }
  }
  return false;
}

function releaseLock() {
  if (lockDepth > 1) { lockDepth--; return; }
  if (lockDepth === 1) {
    lockDepth = 0;
    try { fs.rmdirSync(lockDirPath()); } catch (e) { /* best effort */ }
  }
}

/** Run fn under the ledger lock; CLI paths hard-fail if the lock stays contended. */
function withLock(fn) {
  if (!acquireLock()) {
    fail('could not acquire ledger lock (' + lockDirPath() + '). Another coderecall process may be busy; retry, or delete the .lock directory if stale.');
  }
  try { return fn(); } finally { releaseLock(); }
}

