/** Recursive delete (Node 10.12 floor: no fs.rmSync). Best-effort. */
function rmrf(target) {
  let st;
  try { st = fs.lstatSync(target); } catch (e) { return; }
  if (st.isDirectory()) {
    for (const name of fs.readdirSync(target)) rmrf(path.join(target, name));
    try { fs.rmdirSync(target); } catch (e) { /* best effort */ }
  } else {
    try { fs.unlinkSync(target); } catch (e) { /* best effort */ }
  }
}

/** Best-effort remove a directory only if it is now empty. */
function rmdirIfEmpty(dir) {
  try { if (fs.readdirSync(dir).length === 0) fs.rmdirSync(dir); } catch (e) { /* not empty / absent */ }
}

/**
 * Strip the CODE-RECALL marker section from a file, preserving all other content.
 * Returns a status word. If the file is left with nothing but our scaffold
 * (empty, or just "# AGENTS"), it is deleted.
 */
function stripSection(filePath) {
  const text = readFileSafe(filePath);
  if (text === null) return 'absent';
  const found = findSection(text);
  if (!found) return 'no-section';
  const before = text.slice(0, found.start);
  const after = text.slice(found.endIdx + MARKER_END.length);
  let merged = (before.replace(/\s+$/, '') + '\n' + after.replace(/^\s+/, '')).replace(/\n{3,}/g, '\n\n').replace(/^\s+/, '');
  const t = merged.trim();
  if (t === '' || t === '# AGENTS') {
    try { fs.unlinkSync(filePath); } catch (e) { /* best effort */ }
    return 'removed (was coderecall only)';
  }
  writeFileAtomic(filePath, merged.endsWith('\n') ? merged : merged + '\n');
  return 'section stripped';
}

