function rotateSnapshots() {
  // Keep the newest SNAPSHOT_KEEP snapshots by mtime (not by name): hook and
  // CLI snapshots may carry historic filename formats whose lexical order
  // does not match chronological order, so a name sort can delete new files.
  let names;
  try { names = fs.readdirSync(ARCHIVE_DIR); } catch (e) { return; }
  const snaps = names
    .filter((n) => /^precompact-.*\.md$/.test(n))
    .map((n) => {
      const full = path.join(ARCHIVE_DIR, n);
      let mtime = 0;
      try { mtime = fs.statSync(full).mtimeMs; } catch (e) { mtime = 0; }
      return { full, name: n, mtime };
    })
    .sort((a, b) => b.mtime - a.mtime || b.name.localeCompare(a.name));
  for (const old of snaps.slice(SNAPSHOT_KEEP)) {
    try { fs.unlinkSync(old.full); } catch (e) { /* best effort */ }
  }
}

function writeSnapshot(trigger, extraLines) {
  ensureDir(ARCHIVE_DIR);
  // pid suffix: two processes snapshotting in the same second must not collide.
  const file = path.join(ARCHIVE_DIR, 'precompact-' + tsForFilename() + '-' + process.pid + '.md');
  const taskText = readFileSafe(TASK_FILE);
  const parts = [
    '# coderecall snapshot',
    '- trigger: ' + trigger,
    '- time: ' + nowIso(),
    '',
  ];
  if (extraLines && extraLines.length) {
    parts.push('## Recent activity', sanitize(extraLines.join('\n')), '');
  }
  if (taskText !== null) {
    parts.push('## TASK.md at snapshot time', '', sanitize(taskText.replace(/\r\n/g, '\n')));
  } else {
    parts.push('(TASK.md missing at snapshot time)');
  }
  writeFileAtomic(file, parts.join('\n') + '\n');
  rotateSnapshots();
  return file;
}

