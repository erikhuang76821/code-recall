/**
 * Merge into a JSON config file: parse (skip if invalid/non-object), let the
 * mutator mutate in place and report whether it changed anything, then write
 * pretty JSON atomically. Preserves all unrelated keys. Returns a status word.
 */
function mergeJsonFile(dest, mutator) {
  ensureDir(path.dirname(dest));
  const existing = readFileSafe(dest);
  let obj = {};
  if (existing !== null) {
    try { obj = JSON.parse(existing); } catch (e) { return 'skipped (not valid JSON)'; }
    if (!obj || typeof obj !== 'object' || Array.isArray(obj)) return 'skipped (not a JSON object)';
  }
  const changed = mutator(obj);
  writeFileAtomic(dest, JSON.stringify(obj, null, 2) + '\n');
  return existing === null ? 'created' : (changed ? 'updated' : 'unchanged');
}

