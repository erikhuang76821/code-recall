function consolidateLocked() {
  let report = [];

  // 1. Move completed [x] checklist items to archive/
  //
  // Counting (parseTask) is lenient — /^\s*- \[([ >x!])\]/ includes indented
  // sub-items. Archiving is deliberately conservative: only TOP-LEVEL done
  // items move, and they take their entire indented block with them, but only
  // when no indented child checklist item is still open ([ ], [>], [!]).
  // A done parent with unfinished children stays put so context isn't lost.
  const taskText = readFileSafe(TASK_FILE);
  if (taskText !== null) {
    const lines = splitLines(taskText);
    const keptLines = [];
    const archivedLines = [];
    let archivedItems = 0;
    let i = 0;
    while (i < lines.length) {
      if (/^- \[x\]/.test(lines[i])) {
        // Collect the contiguous indented block belonging to this item.
        // Blank lines INSIDE the block are allowed (and kept with it) as long
        // as the next non-blank line is still indented; otherwise the block
        // ends before the blank run.
        let j = i + 1;
        const block = [];
        while (j < lines.length) {
          if (/^\s+\S/.test(lines[j])) { block.push(lines[j]); j++; continue; }
          if (/^\s*$/.test(lines[j])) {
            let k = j;
            while (k < lines.length && /^\s*$/.test(lines[k])) k++;
            if (k < lines.length && /^\s+\S/.test(lines[k])) {
              while (j < k) { block.push(lines[j]); j++; }
              continue;
            }
          }
          break;
        }
        const hasOpenChild = block.some((l) => {
          const m = /^\s*- \[([ >x!])\]/.exec(l);
          return m && m[1] !== 'x';
        });
        if (!hasOpenChild) {
          archivedLines.push(lines[i]);
          for (const b of block) archivedLines.push(b);
          archivedItems++;
          i = j;
          continue;
        }
      }
      keptLines.push(lines[i]);
      i++;
    }
    if (archivedItems > 0) {
      // Monthly rollup (consolidated-YYYY-MM.md), not per-day: keeps completed
      // work coalesced into a small, searchable summary file instead of
      // scattering one file per consolidation day. Nothing is deleted — the
      // items move here and stay indexable by `memo search`.
      const archFile = path.join(ARCHIVE_DIR, 'consolidated-' + todayDate().slice(0, 7) + '.md');
      const prev = readFileSafe(archFile) || '# Consolidated checklist items\n';
      writeFileAtomic(archFile, prev.replace(/\n+$/, '\n') + '\n## ' + nowIso() + '\n' + archivedLines.join('\n') + '\n');
      writeFileAtomic(TASK_FILE, keptLines.join('\n'));
      report.push('archived ' + archivedItems + ' done item(s) (incl. indented children) -> ' + path.relative(CWD, archFile));
    } else {
      report.push('no completed checklist items to archive');
    }
    touchTaskUpdated();
  }

  // 2. Dedupe + age-flag DECISIONS.md and LESSONS.md
  const cutoff = Date.now() - 90 * 24 * 3600000;
  for (const [label, p] of [['DECISIONS.md', DECISIONS_FILE], ['LESSONS.md', LESSONS_FILE]]) {
    const text = readFileSafe(p);
    if (text === null) continue;
    const parsed = parseEntries(text);
    // Dedupe: keep newest-dated entry of any overlapping pair.
    const keep = [];
    let deduped = 0;
    for (const e of parsed.entries) {
      let merged = false;
      for (let i = 0; i < keep.length; i++) {
        if (titleOverlap(keep[i].title, e.title) > TITLE_OVERLAP_THRESHOLD) {
          const dOld = entryDate(keep[i]) || '';
          const dNew = entryDate(e) || '';
          if (dNew >= dOld) keep[i] = e;  // newer (or undated-later) wins
          deduped++;
          merged = true;
          break;
        }
      }
      if (!merged) keep.push(e);
    }
    // Flag entries older than 90 days as confidence: low (needs re-verification).
    let flagged = 0;
    for (const e of keep) {
      const d = entryDate(e);
      if (d && Date.parse(d) < cutoff) {
        const before = e.lines.join('\n');
        setEntryField(e, 'confidence', 'low');
        if (e.lines.join('\n') !== before) flagged++;
      }
    }
    parsed.entries = keep;
    writeFileAtomic(p, serializeEntries(parsed));
    report.push(label + ': ' + deduped + ' duplicate(s) merged, ' + flagged + ' stale entry(ies) flagged confidence: low');
  }

  // 3. Refresh AGENTS.md digest after consolidation
  upsertSection(AGENTS_FILE);
  report.push('AGENTS.md marker section refreshed');

  console.log('memo-star consolidate:');
  for (const r of report) console.log('  ' + r);
}

// ---------------------------------------------------------------------------
// Lexical search (zero-dep BM25 over ledger + archive)
//
// Closes the one dimension COMPATIBILITY.md admits we lose to vector stores:
// "semantic recall over months". This is lexical, not semantic, but it makes
// every decision/lesson/snapshot retrievable without a DB, embeddings, or
// network. Read-only: no lock needed.
// ---------------------------------------------------------------------------

/**
 * Tokenize for search/scoring: lowercase alphanumeric word tokens PLUS each CJK
 * character as its own token (zero-dep stand-in for word segmentation, so
 * Chinese queries match without a tokenizer dependency).
 */
function tokenize(text) {
  const s = String(text).toLowerCase();
  const words = s.match(/[a-z0-9]+/g) || [];
  const cjk = s.match(/[一-鿿]/g) || [];
  return words.concat(cjk);
}

/** Split text into non-empty paragraph blocks (blank-line separated). */
function paragraphs(text) {
  return splitLines(text).join('\n').split(/\n[ \t]*\n/).map((s) => s.trim()).filter(Boolean);
}

/**
 * Collect searchable chunks across the ledger + archive:
 * - TASK.md            → paragraph blocks
 * - DECISIONS/LESSONS  → one chunk per "## entry" (label = title)
 * - archive/*.md       → paragraph blocks (label = filename)
 */
function collectChunks() {
  const chunks = [];
  const taskText = readFileSafe(TASK_FILE);
  if (taskText !== null) {
    for (const p of paragraphs(taskText)) chunks.push({ source: 'TASK.md', label: '', text: p });
  }
  for (const [label, p] of [['DECISIONS.md', DECISIONS_FILE], ['LESSONS.md', LESSONS_FILE]]) {
    const text = readFileSafe(p);
    if (text === null) continue;
    for (const e of parseEntries(text).entries) {
      chunks.push({ source: label, label: e.title, text: e.lines.join('\n') });
    }
  }
  let archNames = [];
  try { archNames = fs.readdirSync(ARCHIVE_DIR); } catch (e) { archNames = []; }
  for (const n of archNames) {
    if (!/\.md$/.test(n)) continue;
    const text = readFileSafe(path.join(ARCHIVE_DIR, n));
    if (text === null) continue;
    for (const p of paragraphs(text)) {
      chunks.push({ source: path.join('archive', n), label: '', text: p });
    }
  }
  return chunks;
}

/** Classic BM25 (k1=1.5, b=0.75) over the chunk corpus. Returns scored desc. */
function bm25Search(query, chunks, limit) {
  const qset = Array.from(new Set(tokenize(query)));
  if (qset.length === 0) return [];
  const docs = chunks.map((c) => ({ chunk: c, toks: tokenize(c.text) }));
  const N = docs.length || 1;
  const df = Object.create(null);
  for (const d of docs) {
    for (const tok of new Set(d.toks)) df[tok] = (df[tok] || 0) + 1;
  }
  const avgdl = docs.reduce((a, d) => a + d.toks.length, 0) / N || 1;
  const k1 = 1.5, b = 0.75;
  const scored = [];
  for (const d of docs) {
    const tf = Object.create(null);
    for (const tok of d.toks) tf[tok] = (tf[tok] || 0) + 1;
    let score = 0;
    for (const term of qset) {
      const f = tf[term];
      if (!f) continue;
      const idf = Math.log(1 + (N - df[term] + 0.5) / (df[term] + 0.5));
      score += idf * (f * (k1 + 1)) / (f + k1 * (1 - b + b * (d.toks.length / avgdl)));
    }
    if (score > 0) scored.push({ chunk: d.chunk, score });
  }
  scored.sort((a, b2) => b2.score - a.score);
  return scored.slice(0, limit);
}

function cmdSearch(query, limit) {
  requireLedger();
  if (!query || !query.trim()) fail('usage: node memo.js search <query> [--limit N]');
  const results = bm25Search(query, collectChunks(), limit || SEARCH_LIMIT_DEFAULT);
  if (results.length === 0) {
    console.log('No matches for: ' + query);
    return;
  }
  console.log('memo-star search: "' + query + '" — ' + results.length + ' result(s)');
  console.log('');
  for (const r of results) {
    const head = r.chunk.source + (r.chunk.label ? '  › ' + r.chunk.label : '');
    console.log(r.score.toFixed(2) + '  ' + head);
    const snippet = r.chunk.text.split('\n').map((l) => l.trim()).filter(Boolean).slice(0, 3);
    for (const l of snippet) console.log('      ' + (l.length > 150 ? l.slice(0, 150) + ' …' : l));
    console.log('');
  }
}

