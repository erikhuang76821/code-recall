// ---------------------------------------------------------------------------
// Git pre-commit integration (optional; code maintains derived state)
//
// Rationale: derived/index state should be regenerated by code, not by the
// agent's goodwill. This hook refreshes the AGENTS.md digest and lints the
// ledger at commit time. Implemented as memo.js subcommands (node writes the
// hook file) so it behaves identically on Windows (Git for Windows runs hooks
// through its bundled sh) and POSIX — no per-OS shell duplication.
// ---------------------------------------------------------------------------
const GITHOOK_BEGIN = '# >>> memo-star >>> (managed block — installed by: node memo.js install-githook)';
const GITHOOK_END = '# <<< memo-star <<<';

/** Run `git <args>` in CWD, returning trimmed stdout, or null on any failure. */
function gitOut(args) {
  try {
    return require('child_process').execFileSync('git', args, { cwd: CWD, encoding: 'utf8', stdio: ['ignore', 'pipe', 'ignore'] }).trim();
  } catch (e) { return null; }
}

/** Resolve the repo's hooks directory (honors core.hooksPath), or null. */
function gitHooksDir() {
  const top = gitOut(['rev-parse', '--show-toplevel']);
  if (top === null) return null;
  let hooks = gitOut(['rev-parse', '--git-path', 'hooks']);
  if (hooks === null) return null;
  // --git-path may return a path relative to CWD; resolve against CWD.
  return path.resolve(CWD, hooks);
}

/** Forward-slash an absolute path (sh-friendly, valid for node on Windows too). */
function fwd(p) { return String(p).replace(/\\/g, '/'); }

/** The managed sh block embedded in .git/hooks/pre-commit. strict → blocking. */
function gitHookBlock(strict) {
  const node = fwd(process.execPath);
  const memo = fwd(path.join(__dirname, 'memo.js'));
  // Shell-safety guard mirrors the installers: a path with " ` $ or a newline
  // could break out of the double-quoting in the generated sh.
  if (/["`$\n\r]/.test(node) || /["`$\n\r]/.test(memo)) {
    fail('cannot install git hook: a path contains shell-unsafe characters (" ` $ or newline)\n  node: ' + node + '\n  memo: ' + memo);
  }
  return [
    GITHOOK_BEGIN,
    'if [ -d .ai/memory ]; then',
    '  _memo_node="' + node + '"',
    '  [ -x "$_memo_node" ] || _memo_node="$(command -v node 2>/dev/null)"',
    '  if [ -n "$_memo_node" ]; then',
    '    "$_memo_node" "' + memo + '" precommit' + (strict ? ' --strict' : '') + ' || exit 1',
    '    # Stage the refreshed digest for files git already tracks (so the',
    '    # committed AGENTS.md/CLAUDE.md match TASK.md). Untracked files are',
    '    # left alone. if/then/fi (not &&) keeps exit status 0 on a non-match,',
    '    # otherwise a final non-zero check would abort the whole commit.',
    '    for _memo_f in AGENTS.md CLAUDE.md; do',
    '      if git ls-files --cached --error-unmatch "$_memo_f" >/dev/null 2>&1; then git add "$_memo_f"; fi',
    '    done',
    '  fi',
    'fi',
    GITHOOK_END,
  ].join('\n');
}

function cmdPrecommit(strict) {
  requireLedger();
  // 1. Refresh derived state so the committed digest matches TASK.md.
  upsertSection(AGENTS_FILE);
  ensureClaudeStub();
  // 2. Lint + budget (budget is advisory; lint can block under --strict).
  const issues = ledgerLintIssues();
  const overBudget = [];
  for (const [label, p] of [['TASK.md', TASK_FILE], ['DECISIONS.md', DECISIONS_FILE], ['LESSONS.md', LESSONS_FILE]]) {
    const text = readFileSafe(p);
    if (text !== null && Buffer.byteLength(text, 'utf8') > LEDGER_WARN_BYTES) overBudget.push(label);
  }
  console.log('memo-star pre-commit: AGENTS.md digest refreshed.');
  if (overBudget.length) console.log('  note: over ~' + LEDGER_WARN_BYTES + 'B (run: node memo.js consolidate): ' + overBudget.join(', '));
  if (issues.length === 0) {
    console.log('  lint: clean.');
    return;
  }
  // Prominent banner (no ANSI: a git hook's stdout is not reliably a TTY, and
  // colour codes would garble on Windows cmd / non-interactive shells).
  const bar = '  !! ' + '-'.repeat(60);
  console.log(bar);
  console.log('  !! memo-star lint: ' + issues.length + ' issue(s) in the ledger' +
    (strict ? ' — (--strict) BLOCKING this commit' : ' — commit ALLOWED (advisory)'));
  for (const m of issues) console.log('  !!   - ' + m);
  console.log('  !! Fix the entries' + (strict ? ', or bypass once: git commit --no-verify' : ' (run: node memo.js doctor)') + '.');
  console.log(bar);
  if (strict) process.exit(1);
}

function cmdInstallGitHook(strict) {
  requireLedger();
  const hooksDir = gitHooksDir();
  if (hooksDir === null) fail('not a git repository (or git not found). Run inside a repo, or `git init` first.');
  ensureDir(hooksDir);
  const file = path.join(hooksDir, 'pre-commit');
  const block = gitHookBlock(strict);
  const existing = readFileSafe(file);
  let out;
  let verb;
  if (existing === null) {
    out = '#!/bin/sh\n' + block + '\n';
    verb = 'created';
  } else {
    const norm = existing.replace(/\r\n/g, '\n');
    const b = norm.indexOf(GITHOOK_BEGIN);
    if (b !== -1) {
      const e = norm.indexOf(GITHOOK_END, b);
      const end = e === -1 ? norm.length : e + GITHOOK_END.length;
      out = norm.slice(0, b) + block + norm.slice(end);
      verb = 'updated (replaced existing memo-star block)';
    } else {
      out = norm.replace(/\n*$/, '\n') + '\n' + block + '\n';
      verb = 'appended (preserved your existing pre-commit hook)';
    }
  }
  writeFileAtomic(file, out);
  try { fs.chmodSync(file, 0o755); } catch (e) { /* Windows / no-op */ }
  console.log('memo-star git pre-commit hook ' + verb + ' [' + (strict ? 'strict: lint errors block the commit' : 'advisory: lint errors warn only') + ']:');
  console.log('  ' + file);
  console.log('It runs `memo precommit' + (strict ? ' --strict' : '') + '` (refresh digest + lint) and re-stages AGENTS.md/CLAUDE.md when they are part of the commit.');
  console.log((strict ? 'Re-run without --strict for advisory mode. ' : 'Re-run with --strict to make malformed entries block commits. ') + 'Remove with: node memo.js remove-githook');
}

/** Strip our managed block from .git/hooks/pre-commit. Returns a status word. */
function stripGitHookFile() {
  const hooksDir = gitHooksDir();
  if (hooksDir === null) return 'no-repo';
  const file = path.join(hooksDir, 'pre-commit');
  const existing = readFileSafe(file);
  if (existing === null) return 'absent';
  const norm = existing.replace(/\r\n/g, '\n');
  const b = norm.indexOf(GITHOOK_BEGIN);
  if (b === -1) return 'no-block';
  const e = norm.indexOf(GITHOOK_END, b);
  const end = e === -1 ? norm.length : e + GITHOOK_END.length;
  const stripped = (norm.slice(0, b) + norm.slice(end)).replace(/\n{3,}/g, '\n\n');
  // If nothing but a shebang (or whitespace) remains, remove the file entirely.
  if (stripped.replace(/^#!.*\n?/, '').trim() === '') {
    try { fs.unlinkSync(file); } catch (e2) { /* best effort */ }
    return 'removed';
  }
  writeFileAtomic(file, stripped.endsWith('\n') ? stripped : stripped + '\n');
  return 'stripped';
}

function cmdRemoveGitHook() {
  const r = stripGitHookFile();
  if (r === 'no-repo') fail('not a git repository (or git not found).');
  console.log({
    'absent': 'No pre-commit hook present; nothing to remove.',
    'no-block': 'pre-commit hook has no memo-star block; left untouched.',
    'removed': 'Removed memo-star pre-commit hook (file held only our block).',
    'stripped': 'Stripped memo-star block; your other pre-commit logic was preserved.',
  }[r]);
}

