/**
 * Append one entry to the bounded sessions.md timeline (answers "what was I
 * doing last Tuesday"). Consecutive identical NOW values are de-duplicated so
 * a per-turn Stop hook doesn't spam the file. Whole-file atomic replace, no
 * lock: last-writer-wins is acceptable for a timeline (a rare concurrent Stop
 * may drop one line; the hook must never block).
 */
function appendSession() {
  const t = parseTask(readFileSafe(TASK_FILE) || '');
  if (!t) return;
  const now = (t.now || '(no NOW set)').replace(/[\r\n]+/g, ' ').trim();
  const clean = sanitize(now).slice(0, 160);
  const existing = readFileSafe(SESSIONS_FILE);
  let entryLines = [];
  if (existing !== null) {
    entryLines = splitLines(existing).filter((l) => /^- \d{4}-\d{2}-\d{2}T/.test(l));
    const last = entryLines[entryLines.length - 1];
    if (last) {
      const m = /— (.*)$/.exec(last);
      if (m && m[1] === clean) return; // unchanged NOW since last stop — skip
    }
  }
  entryLines.push('- ' + nowIso() + ' — ' + clean);
  if (entryLines.length > SESSIONS_KEEP) entryLines = entryLines.slice(entryLines.length - SESSIONS_KEEP);
  writeFileAtomic(SESSIONS_FILE, '# Sessions timeline\n\n' + entryLines.join('\n') + '\n');
}

/**
 * Build the optional UserPromptSubmit staleness reminder, or null. Fires only
 * when TASK.md UPDATED is older than STALE_REMINDER_MINUTES, throttled to at
 * most once per that same window (timestamp in .reminder). Best-effort write:
 * if .reminder can't be written, still return the reminder once.
 */
function buildStaleReminder() {
  const t = parseTask(readFileSafe(TASK_FILE) || '');
  const age = taskAgeHours(t);
  if (age === null) return null;
  const ageMin = age * 60;
  if (ageMin < STALE_REMINDER_MINUTES) return null;
  const last = readFileSafe(REMINDER_FILE);
  if (last) {
    const lt = Date.parse(last.trim());
    if (!isNaN(lt) && (Date.now() - lt) < STALE_REMINDER_MINUTES * 60000) return null;
  }
  try { writeFileAtomic(REMINDER_FILE, nowIso()); } catch (e) { /* read-only fs */ }
  return '[memo-star] Ledger ~' + Math.round(ageMin) + 'min stale — re-check .ai/memory/TASK.md and update NOW:/NEXT:.';
}

