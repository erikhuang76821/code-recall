function consolidateLocked() {
  let report = [];

  // 1. Move completed [x] checklist items to archive/
  //
  // Counting (parseTask) is lenient — /^\s*- \[([ >x!])\]/ includes indented
  // sub-items. Archiving is deliberately conservative: only TOP-LEVEL done
  // items move, and they take their entire indented block with them, but only
  // when no indented child checklist item is still open ([ ], [>], [!]).
  // A done parent with unfinished children stays put so context isn't lost.
  const taskText = readFileSafe(TASK_FILE);
  if (taskText !== null) {
    const lines = splitLines(taskText);
    const keptLines = [];
    const archivedLines = [];
    let archivedItems = 0;
    let i = 0;
    while (i < lines.length) {
      if (/^- \[x\]/.test(lines[i])) {
        // Collect the contiguous indented block belonging to this item.
        // Blank lines INSIDE the block are allowed (and kept with it) as long
        // as the next non-blank line is still indented; otherwise the block
        // ends before the blank run.
        let j = i + 1;
        const block = [];
        while (j < lines.length) {
          if (/^\s+\S/.test(lines[j])) { block.push(lines[j]); j++; continue; }
          if (/^\s*$/.test(lines[j])) {
            let k = j;
            while (k < lines.length && /^\s*$/.test(lines[k])) k++;
            if (k < lines.length && /^\s+\S/.test(lines[k])) {
              while (j < k) { block.push(lines[j]); j++; }
              continue;
            }
          }
          break;
        }
        const hasOpenChild = block.some((l) => {
          const m = /^\s*- \[([ >x!])\]/.exec(l);
          return m && m[1] !== 'x';
        });
        if (!hasOpenChild) {
          archivedLines.push(lines[i]);
          for (const b of block) archivedLines.push(b);
          archivedItems++;
          i = j;
          continue;
        }
      }
      keptLines.push(lines[i]);
      i++;
    }
    if (archivedItems > 0) {
      const archFile = path.join(ARCHIVE_DIR, 'consolidated-' + todayDate() + '.md');
      const prev = readFileSafe(archFile) || '# Consolidated checklist items\n';
      writeFileAtomic(archFile, prev.replace(/\n+$/, '\n') + '\n## ' + nowIso() + '\n' + archivedLines.join('\n') + '\n');
      writeFileAtomic(TASK_FILE, keptLines.join('\n'));
      report.push('archived ' + archivedItems + ' done item(s) (incl. indented children) -> ' + path.relative(CWD, archFile));
    } else {
      report.push('no completed checklist items to archive');
    }
    touchTaskUpdated();
  }

  // 2. Dedupe + age-flag DECISIONS.md and LESSONS.md
  const cutoff = Date.now() - 90 * 24 * 3600000;
  for (const [label, p] of [['DECISIONS.md', DECISIONS_FILE], ['LESSONS.md', LESSONS_FILE]]) {
    const text = readFileSafe(p);
    if (text === null) continue;
    const parsed = parseEntries(text);
    // Dedupe: keep newest-dated entry of any overlapping pair.
    const keep = [];
    let deduped = 0;
    for (const e of parsed.entries) {
      let merged = false;
      for (let i = 0; i < keep.length; i++) {
        if (titleOverlap(keep[i].title, e.title) > TITLE_OVERLAP_THRESHOLD) {
          const dOld = entryDate(keep[i]) || '';
          const dNew = entryDate(e) || '';
          if (dNew >= dOld) keep[i] = e;  // newer (or undated-later) wins
          deduped++;
          merged = true;
          break;
        }
      }
      if (!merged) keep.push(e);
    }
    // Flag entries older than 90 days as confidence: low (needs re-verification).
    let flagged = 0;
    for (const e of keep) {
      const d = entryDate(e);
      if (d && Date.parse(d) < cutoff) {
        const before = e.lines.join('\n');
        setEntryField(e, 'confidence', 'low');
        if (e.lines.join('\n') !== before) flagged++;
      }
    }
    parsed.entries = keep;
    writeFileAtomic(p, serializeEntries(parsed));
    report.push(label + ': ' + deduped + ' duplicate(s) merged, ' + flagged + ' stale entry(ies) flagged confidence: low');
  }

  // 3. Refresh AGENTS.md digest after consolidation
  upsertSection(AGENTS_FILE);
  report.push('AGENTS.md marker section refreshed');

  console.log('memo-star consolidate:');
  for (const r of report) console.log('  ' + r);
}

// ---------------------------------------------------------------------------
// Entry point
// ---------------------------------------------------------------------------
function main() {
  const args = process.argv.slice(2);
  const cmd = args[0];
  const usage = 'usage: node memo.js <init|sync [--all]|status|doctor|digest|snapshot|consolidate>';
  switch (cmd) {
    case 'init': return cmdInit();
    case 'sync': return cmdSync(args.includes('--all'));
    case 'status': return cmdStatus();
    case 'doctor': return cmdDoctor();
    case 'digest': return cmdDigest();
    case 'snapshot': return cmdSnapshot();
    case 'consolidate': return cmdConsolidate();
    case undefined:
    case '-h':
    case '--help':
    case 'help':
      console.log(usage);
      return;
    default:
      fail('unknown command "' + cmd + '". ' + usage);
  }
}

