function cmdInit() {
  ensureDir(ARCHIVE_DIR);
  const created = [];
  const skipped = [];
  const seed = [
    ['TASK.md', TASK_FILE],
    ['DECISIONS.md', DECISIONS_FILE],
    ['LESSONS.md', LESSONS_FILE],
  ];
  for (const [tplName, dest] of seed) {
    if (fs.existsSync(dest)) { skipped.push(path.relative(CWD, dest)); continue; }
    // Function form: never let the replacement be interpreted as $-patterns.
    const ts = nowIso();
    const content = loadTemplate(tplName).replace('{{TIMESTAMP}}', () => ts);
    writeFileAtomic(dest, content);
    created.push(path.relative(CWD, dest));
  }
  const agentsResult = upsertSection(AGENTS_FILE);
  const claudeResult = ensureClaudeStub();

  console.log('memo-star init complete.');
  if (created.length) console.log('  created: ' + created.join(', '));
  if (skipped.length) console.log('  kept existing: ' + skipped.join(', '));
  console.log('  AGENTS.md marker section: ' + agentsResult);
  console.log('  CLAUDE.md stub: ' + claudeResult);
  console.log('');
  console.log('Next step (once per machine): run install.ps1 (Windows) or install.sh');
  console.log('to register the Claude Code hooks in ~/.claude/settings.json.');
  console.log('Optional: node memo.js sync --all  (stubs for Cursor/Windsurf/Cline/Roo/Copilot/Gemini)');
}

function cmdSync(all) {
  requireLedger();
  const agentsResult = upsertSection(AGENTS_FILE);
  console.log('AGENTS.md marker section: ' + agentsResult);
  const claudeResult = ensureClaudeStub();
  console.log('CLAUDE.md stub: ' + claudeResult);
  if (all) {
    for (const stub of STUBS) {
      const dest = path.join(CWD, stub.rel);
      ensureDir(path.dirname(dest));
      if (stub.shared) {
        // Shared file (may contain user content): marker-section upsert only.
        const res = upsertSectionIn(dest, stub.content.replace(/\n+$/, ''), '');
        console.log('stub section ' + res + ': ' + stub.rel);
      } else {
        const existed = fs.existsSync(dest);
        writeFileAtomic(dest, stub.content);
        console.log('stub ' + (existed ? 'refreshed' : 'created') + ': ' + stub.rel);
      }
    }
    console.log('Gemini CLI tip: you may instead set context.fileName: AGENTS.md in Gemini settings.');
  }
}

function cmdStatus() {
  requireLedger();
  const taskText = readFileSafe(TASK_FILE);
  if (taskText === null) fail('TASK.md missing. Run: node memo.js init');
  const t = parseTask(taskText);

  console.log('memo-star status — ' + CWD);
  console.log('');
  console.log('GOAL: ' + t.goal);
  console.log('NOW:  ' + t.now);
  console.log('NEXT: ' + t.next);
  console.log('UPDATED: ' + (t.updated || '(none)'));
  const age = taskAgeHours(t);
  if (age !== null) {
    const staleTag = age > STALE_HOURS ? '  [STALE > ' + STALE_HOURS + 'h]' : '';
    console.log('Age: ' + age.toFixed(1) + 'h' + staleTag);
  }
  if (heartbeatStale()) console.log('Heartbeat: STALE flag set — verify ledger before trusting.');
  console.log('');
  console.log('Checklist: ' + t.counts.todo + ' todo, ' + t.counts.doing + ' doing, ' +
    t.counts.done + ' done, ' + t.counts.blocked + ' blocked');
  console.log('');
  console.log('Token estimates (chars/4, budget ' + LEDGER_TOKEN_BUDGET + '/file):');
  for (const [label, p] of [['TASK.md', TASK_FILE], ['DECISIONS.md', DECISIONS_FILE], ['LESSONS.md', LESSONS_FILE]]) {
    const text = readFileSafe(p);
    if (text === null) { console.log('  ' + label + ': MISSING'); continue; }
    const est = tokenEstimate(text);
    const over = est > LEDGER_TOKEN_BUDGET ? '  [OVER BUDGET]' : '';
    console.log('  ' + label + ': ~' + est + ' tokens' + over);
  }
  console.log('');
  const drift = sectionDrift(AGENTS_FILE);
  const driftMsg = {
    'ok': 'in sync',
    'drift': 'DRIFT — section edited by hand; run: node memo.js sync',
    'missing-section': 'MISSING — run: node memo.js sync',
    'missing-file': 'AGENTS.md MISSING — run: node memo.js sync',
  }[drift];
  console.log('AGENTS.md marker section: ' + driftMsg);
}

function cmdDoctor() {
  requireLedger();
  let warnings = 0;
  const warn = (msg) => { warnings++; console.log('  WARN ' + msg); };
  const ok = (msg) => console.log('  ok   ' + msg);
  console.log('memo-star doctor');
  console.log('');

  // 1. Ledger files + budgets
  console.log('[ledger]');
  for (const [label, p] of [['TASK.md', TASK_FILE], ['DECISIONS.md', DECISIONS_FILE], ['LESSONS.md', LESSONS_FILE]]) {
    const text = readFileSafe(p);
    if (text === null) { warn(label + ' missing — run: node memo.js init'); continue; }
    const bytes = Buffer.byteLength(text, 'utf8');
    if (bytes > LEDGER_WARN_BYTES) warn(label + ' is ' + bytes + ' bytes (> ' + LEDGER_WARN_BYTES + '); run: node memo.js consolidate');
    else ok(label + ' ' + bytes + ' bytes (~' + tokenEstimate(text) + ' tokens)');
  }
  const t = parseTask(readFileSafe(TASK_FILE) || '');
  const age = taskAgeHours(t);
  if (age === null) warn('TASK.md UPDATED line missing or unparseable');
  else if (age > STALE_HOURS && t.goal && !t.goal.startsWith('<')) warn('TASK.md is ' + age.toFixed(1) + 'h old — ledger may be stale');
  else ok('TASK.md freshness ' + age.toFixed(1) + 'h');

  // 2. Marker section drift
  console.log('[sync]');
  const drift = sectionDrift(AGENTS_FILE);
  if (drift === 'ok') ok('AGENTS.md marker section checksum matches');
  else warn('AGENTS.md marker section: ' + drift + ' — run: node memo.js sync');

  // 3. Hooks in ~/.claude/settings.json
  // (path overridable via MEMO_STAR_SETTINGS for testing; default unchanged)
  console.log('[hooks]');
  const settingsPath = process.env.MEMO_STAR_SETTINGS ||
    path.join(os.homedir(), '.claude', 'settings.json');
  const settingsText = readFileSafe(settingsPath);
  let sessionStartCmd = null;
  if (settingsText === null) {
    warn('~/.claude/settings.json not found — run install.ps1 to register hooks');
  } else {
    let settings = null;
    try { settings = JSON.parse(settingsText); } catch (e) {
      warn('~/.claude/settings.json is not valid JSON: ' + e.message);
    }
    if (settings) {
      const hooks = settings.hooks || {};
      // Two-way detection mirroring install.ps1/install.sh: an entry is ours if
      // its command mentions "memo-star" OR this repo's own hooks directory
      // (covers repos checked out under a folder not named memo-star).
      // JSON-escape the path needle because the haystack is JSON.stringify'd
      // (backslashes in Windows paths are doubled there). Compare lowercased —
      // win32 paths are case-insensitive; on POSIX this is merely lenient.
      const hooksDirNeedle = JSON.stringify(path.join(__dirname, 'hooks')).slice(1, -1).toLowerCase();
      for (const event of ['SessionStart', 'PreCompact', 'Stop']) {
        const list = Array.isArray(hooks[event]) ? hooks[event] : [];
        let registered = false;
        for (const entry of list) {
          let es = '';
          try { es = JSON.stringify(entry || {}).toLowerCase(); } catch (e) { es = ''; }
          if (es.includes('memo-star') || es.includes(hooksDirNeedle)) {
            registered = true;
            if (event === 'SessionStart' && sessionStartCmd === null &&
                entry && Array.isArray(entry.hooks)) {
              for (const h of entry.hooks) {
                if (h && typeof h.command === 'string' && h.command) {
                  sessionStartCmd = h.command;
                  break;
                }
              }
            }
          }
        }
        if (registered) ok(event + ' hook registered');
        else warn(event + ' hook not registered — run install.ps1');
      }
    }
  }

  // 4. Hook execution (end-to-end): actually run the registered SessionStart
  // command through a shell (cmd on Windows, sh on POSIX — execSync default)
  // with synthetic hook stdin pointing at a temp cwd WITHOUT .ai/memory, so a
  // healthy hook exits 0 instantly. Catches dead node paths, moved repos, etc.
  console.log('[hook execution]');
  if (sessionStartCmd === null) {
    console.log('  info hook execution check skipped (no memo-star SessionStart hook registered)');
  } else {
    const cp = require('child_process');
    let tmpDir = null;
    try {
      tmpDir = fs.mkdtempSync(path.join(os.tmpdir(), 'memo-star-doctor-'));
      const stdinJson = JSON.stringify({
        hook_event_name: 'SessionStart',
        source: 'startup',
        cwd: tmpDir,
      });
      cp.execSync(sessionStartCmd, {
        input: stdinJson,
        timeout: 5000,
        cwd: tmpDir,
        stdio: ['pipe', 'pipe', 'pipe'],
      });
      ok('SessionStart hook command executed successfully (exit 0)');
    } catch (e) {
      const detail = e && e.stderr ? String(e.stderr).trim() : '';
      warn('SessionStart hook command FAILED: ' + (e && e.message ? e.message : String(e)) +
        (detail ? ' — ' + detail.slice(0, 200) : ''));
    } finally {
      if (tmpDir) { try { fs.rmdirSync(tmpDir); } catch (e2) { /* best effort */ } }
    }
  }

  console.log('');
  console.log(warnings === 0 ? 'All checks passed.' : warnings + ' warning(s). See above.');
}

